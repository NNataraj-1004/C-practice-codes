#include<iostream>
using namespace std;
int main(){

    int x=1,y=1;
    //increement
    //pre
    cout<<x<<endl;
    cout<<++x<<endl;
    cout<<x<<"\n\n";
    //post
    cout<<y<<endl;
    cout<<y++<<endl;
    cout<<y<<"\n\n";
    //decreement
    //pre
    cout<<x<<endl;
    cout<<--x<<endl;
    cout<<x<<"\n\n";
    //post 
    cout<<y<<endl;
    cout<<y--<<endl;
    cout<<y<<"\n";
    return 0; 
}
